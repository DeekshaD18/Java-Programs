package com.Deeksha;

public class Ceiling {
    private int painterColor;
    private int height;

    public Ceiling(int painterColor, int height) {
        this.painterColor = painterColor;
        this.height = height;
    }


    public int getPainterColor() {
        return painterColor;
    }

    public int getHeight() {
        return height;
    }
}
